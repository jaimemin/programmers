SELECT
    APNT_NO,
    PT_NAME,
    PATIENT.PT_NO,
    DOCTOR.MCDP_CD,
    DR_NAME,
    APNT_YMD
FROM
    PATIENT
INNER JOIN
    APPOINTMENT
ON
    APPOINTMENT.PT_NO = PATIENT.PT_NO
INNER JOIN
    DOCTOR
ON
    APPOINTMENT.MDDR_ID = DOCTOR.DR_ID
WHERE
    APNT_CNCL_YN = 'N'
AND
    APNT_YMD LIKE "2022-04-13%"
ORDER BY
    APNT_YMD
